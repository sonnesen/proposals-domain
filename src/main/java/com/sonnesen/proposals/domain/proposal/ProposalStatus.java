package com.sonnesen.proposals.domain.proposal;

/**
 * Enum representing the status of a proposal.
 *
 * IN_PROGRESS: The proposal is currently being processed.
 * APPROVED: The proposal has been approved.
 * REJECTED: The proposal has been rejected.
 * CANCELLED: The proposal has been cancelled.
 */
public enum ProposalStatus {
    IN_PROGRESS, APPROVED, REJECTED, CANCELLED
}
